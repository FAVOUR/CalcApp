class Calculator {

    fun add(i:Double ,z:Double ):Double{

        return  i+z
    }


    fun div(i:Double ,z:Double ):Double{

        return  i/z
    }


    fun sub(i:Double ,z:Double ):Double{

        return  i-z
    }


    fun mul(i:Double ,z:Double ):Double{

        return  i*z
    }
}